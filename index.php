                        <br><br>
                        Dentre os principais esforços para agilizar as mudanças está a criação de um centro de estudos e pesquisa
                        voltado para a indústria 4.0, com o propósito de preparar as empresas para as inovações necessárias.
                        <br><br>
                        Chamado C4IR Brasil, o local deveria começar a funcionar ainda no primeiro semestre de 2020, em São
                        Paulo/SP, segundo aponta esta matéria.
                        <br><br>
                        No entanto, dificuldades relativas à pandemia pelo coronavírus e decorrente isolamento social parecem 
                        ter atrasado o projeto.
                        <br><br>
                        Contudo, algumas medidas da Agenda Brasileira para a Indústria 4.0 seguem sendo implementadas, a exemplo 
                        da conexão entre startups e indústrias.
                        <br><br>
                        Inclusive, esse trabalho conjunto tem auxiliado no combate à propagação do coronavírus desde março de 2020.
                        <br>
                        Em Belo Horizonte/MG, a startup 3DLopes auxiliou o Hospital das Clínicas de BH, produzindo
                        máscaras transparentes de proteção para as equipes de saúde em tempo recorde, graças a tecnologias como 
                        a impressão 3D.
                        <br><br>
                        Embora a gravidade das complicações dessa emergência de saúde pública tenha favorecido alguns progressos, 
