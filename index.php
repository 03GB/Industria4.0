                  <h3>O que é a Indústria 4.0?</h3>
                  <h6> <b> Indústria 4.0 o que é </b> </h6>
                  <br>
                  <img src="./img/foto1.jpeg" alt="Indústria 4.0" width="900px">
                  <br><br>
                  Indústria 4.0 é um conceito que engloba automação e tecnologia da informação, além das principais inovações
                  tecnológicas desses campos.
                  <br>
                  Tudo isso aplicado à manufatura – entendendo o termo como sendo a transformação de matérias-primas em 
                  produtos de valor agregado.
                  <br><br>
                  Pois manufatura, junção das palavras “mão” e “acabamento” em latim, designava um trabalho artesanal, 
                  feito à mão.
                  <br>
                  O que acontece é que a indústria é incompatível com a ideia de fazer as coisas de forma manual.
                  <br><br>
                  Você pode estar pensando “ok, mas qual a novidade disso?”.
                  <br>
                  De fato, já faz muito tempo que as mãos dos operários têm sido substituídas pelas máquinas.
                  <br>
                  Mesmo assim, continuamos avançando na automação, que é a capacidade dessas máquinas trabalharem sem 
                  nenhum operador humano no comando.
                  <br><br>
                  A robótica, com sistemas previamente programados para que os equipamentos desempenhem determinadas 
                  funções sozinhos, também não é muito recente.
                  <br>
                  O que a indústria 4.0 traz é o salto tecnológico de elevar essa automação à máxima potência, permitindo 
                  aos robôs desempenharem funções cada vez mais complexas.
                  <br><br>
                  E não estamos falando apenas do operacional, como soldar duas placas de aço, mas também de tarefas que 
                  pensávamos serem exclusivas de nosso intelecto.
                  <br>
                  São algoritmos que fazem as máquinas analisarem dados em uma velocidade que um humano não conseguiria em 
                  uma vida inteira.
                  <br><br>
                  No final, podemos dizer que a indústria 4.0 é a realidade na qual a tecnologia industrial está cada vez
                  mais eficiente: mais inteligente, mais rápida e mais precisa.
                  <br><br>

