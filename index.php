                        <h3>Como está a Indústria 4.0 hoje no Brasil?</h3>
                        <br>
                        <img src="./img/foto2.jpeg" alt="Indústria 4.0" width="900px">
                        <br><br>
                        Algumas empresas nacionais estão iniciando a jornada rumo à transformação digital, no entanto, o setor 
